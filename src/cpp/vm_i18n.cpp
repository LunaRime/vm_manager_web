VMI18n &VMI18n::Instance()
{
    static VMI18n instance;
    return instance;
}

VMI18n::VMI18n()
{
    /* Locale auto-detection happens via the C layer (LocaleInit).
       We read the detected locale from the C global. */
}

// ============================================================================
// Lookup
// ============================================================================

const VMI18n::Entry *VMI18n::FindEntry(const char *key)
{
    for (const Entry *e = g_entries; e->key; e++) {
        if (strcmp(e->key, key) == 0)
            return e;
    }
    return nullptr;
}

LocaleId VMI18n::GetLocale() const
{
    return LocaleGet();  /* from C locale layer */
}

void VMI18n::SetLocale(LocaleId id)
{
    LocaleSet(id);
}

const char *VMI18n::GetLocaleName() const
{
    switch (GetLocale()) {
    case LOC_ZH_CN: return "zh-CN";
    case LOC_ZH_TW: return "zh-TW";
    default:        return "en-US";
    }
}

// ============================================================================
// ANSI string lookup
// ============================================================================

std::string VMI18n::Utf8ToAnsi(const char *utf8) const
{
    if (!utf8) return "??null??";

    /* Pure ASCII fast path */
    bool isAscii = true;
    for (const char *p = utf8; *p; p++) {
        if ((unsigned char)*p > 0x7F) { isAscii = false; break; }
    }
    if (isAscii) return utf8;

    int wLen = MultiByteToWideChar(CP_UTF8, 0, utf8, -1, nullptr, 0);
    if (wLen <= 0) return utf8;

    WCHAR *wbuf = (WCHAR *)HeapAlloc(GetProcessHeap(), 0, wLen * sizeof(WCHAR));
    if (!wbuf) return utf8;
    MultiByteToWideChar(CP_UTF8, 0, utf8, -1, wbuf, wLen);

    int aLen = WideCharToMultiByte(CP_ACP, 0, wbuf, -1, nullptr, 0, nullptr, nullptr);
    std::string result;
    if (aLen > 0) {
        result.resize(aLen - 1);  /* exclude null terminator */
        WideCharToMultiByte(CP_ACP, 0, wbuf, -1, &result[0], aLen, nullptr, nullptr);
    }

    HeapFree(GetProcessHeap(), 0, wbuf);
    return result.empty() ? utf8 : result;
}

std::wstring VMI18n::Utf8ToWide(const char *utf8) const
{
    if (!utf8) return L"??null??";

    bool isAscii = true;
    for (const char *p = utf8; *p; p++) {
        if ((unsigned char)*p > 0x7F) { isAscii = false; break; }
    }
    if (isAscii) {
        std::wstring out;
        for (const char *p = utf8; *p; p++)
            out += (WCHAR)(unsigned char)*p;
        return out;
    }

    int wLen = MultiByteToWideChar(CP_UTF8, 0, utf8, -1, nullptr, 0);
    if (wLen <= 0) return L"??conv??";

    std::wstring result(wLen - 1, L'\0');
    MultiByteToWideChar(CP_UTF8, 0, utf8, -1, &result[0], wLen);
    return result;
}

std::string VMI18n::Get(const char *key) const
{
    const Entry *e = FindEntry(key);
    if (!e) {
        /* Fallback: try the C locale layer */
        const char *cResult = L10N(key);
        return cResult ? Utf8ToAnsi(cResult) : std::string("??") + key + "??";
    }

    int idx = (int)GetLocale();
    if (idx < 0 || idx > 2) idx = 0;
    const char *val = e->values[idx];

    return val ? Utf8ToAnsi(val) : std::string("??") + key + "??";
}

std::string VMI18n::Format(const char *key, ...) const
{
    std::string tmpl = Get(key);

    va_list args;
    va_start(args, key);

    /* Determine needed buffer size */
    va_list argsCopy;
    va_copy(argsCopy, args);
    int needed = vsnprintf(nullptr, 0, tmpl.c_str(), argsCopy);
    va_end(argsCopy);

    if (needed <= 0) { va_end(args); return tmpl; }

    std::string result(needed + 1, '\0');
    vsnprintf(&result[0], result.size(), tmpl.c_str(), args);
    va_end(args);

    /* Remove null terminator from std::string */
    result.resize(needed);
    return result;
}

// ============================================================================
// Wide string lookup (for Win32 Unicode APIs)
// ============================================================================

const WCHAR *VMI18n::GetW(const char *key) const
{
    /* Ring buffer for wide strings (caller must copy immediately) */
    static WCHAR buf[4][1024];
    static int next = 0;

    std::wstring ws = Utf8ToWide(Get(key).c_str());
    WCHAR *out = buf[next];
    next = (next + 1) % 4;

    int len = (int)ws.size();
    if (len > 1023) len = 1023;
    memcpy(out, ws.c_str(), len * sizeof(WCHAR));
    out[len] = L'\0';
    return out;
}

const WCHAR *VMI18n::FormatW(const char *key, ...) const
{
    va_list args;
    va_start(args, key);

    std::string formatted = Get(key);

    va_list argsCopy;
    va_copy(argsCopy, args);
    int needed = vsnprintf(nullptr, 0, formatted.c_str(), argsCopy);
    va_end(argsCopy);

    std::string result;
    if (needed > 0) {
        result.resize(needed + 1);
        vsnprintf(&result[0], result.size(), formatted.c_str(), args);
        result.resize(needed);
    } else {
        result = formatted;
    }
    va_end(args);

    static WCHAR buf[4][1024];
    static int next = 0;

    std::wstring ws = Utf8ToWide(result.c_str());
    WCHAR *out = buf[next];
    next = (next + 1) % 4;

    int len = (int)ws.size();
    if (len > 1023) len = 1023;
    memcpy(out, ws.c_str(), len * sizeof(WCHAR));
    out[len] = L'\0';
    return out;
}
